#include <stdlib.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		i++;
	}
	return (i);
}

char	*ft_strdup(char *src)
{
	int		src_len;
	int		i;
	char	*new_str;

	src_len = ft_strlen(src);
	i = 0;
	new_str = (char *)malloc(sizeof(char) * (src_len + 1));
	while (src[i])
	{
		new_str[i] = src[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
