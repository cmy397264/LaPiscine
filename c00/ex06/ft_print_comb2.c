#include <unistd.h>

void	print_comb(int i, int j)
{
	if (i != 0 || j != 1)
	{
		write(1, ", ", 2);
	}
	write(1, &"0123456789"[i / 10], 1);
	write(1, &"0123456789"[i % 10], 1);
	write(1, " ", 1);
	write(1, &"0123456789"[j / 10], 1);
	write(1, &"0123456789"[j % 10], 1);
}

void	ft_print_comb2(void)
{
	int	i;
	int	j;

	i = 0;
	while (i < 99)
	{
		j = i + 1;
		while (j < 100)
		{
			print_comb(i, j);
			j++;
		}
		i++;
	}
}
