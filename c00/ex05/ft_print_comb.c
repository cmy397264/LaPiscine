#include <unistd.h>

void	ft_print_comb(void)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	while (i < 8)
	{
		j = i + 1;
		while (j < 9)
		{
			k = j + 1;
			while (k < 10)
			{
				if (i != 0 || j != 1 || k != 2)
				{
					write(1, ", ", 2);
				}
				write(1, &"0123456789"[i], 1);
				write(1, &"0123456789"[j], 1);
				write(1, &"0123456789"[k++], 1);
			}
			j++;
		}
		i++;
	}
}
