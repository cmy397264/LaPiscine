#include <unistd.h>

void	recur_print(int nb)
{
	if (nb > 9)
	{
		recur_print(nb / 10);
	}
	write(1, &"0123456789"[nb % 10], 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	recur_print(nb);
}
